-- FocusFlow Supabase migration: user scoping + RLS
-- Run this in Supabase SQL Editor (or `supabase db push`) once.

begin;

-- 0) Create app tables if this is a fresh Supabase project
create table if not exists public.tasks (
  id integer generated by default as identity primary key,
  user_id uuid,
  title text not null,
  completed boolean not null default false,
  is_priority boolean not null default false,
  date date not null
);

create table if not exists public.habits (
  id integer generated by default as identity primary key,
  user_id uuid,
  name text not null
);

create table if not exists public.habit_logs (
  id integer generated by default as identity primary key,
  user_id uuid,
  habit_id integer not null,
  date date not null,
  completed boolean not null default false
);

create table if not exists public.study_sessions (
  id integer generated by default as identity primary key,
  user_id uuid,
  subject text not null,
  topic text not null,
  duration_minutes integer not null,
  date date not null
);

create table if not exists public.daily_reflections (
  id integer generated by default as identity primary key,
  user_id uuid,
  date date not null,
  content text not null
);

create table if not exists public.weekly_reviews (
  id integer generated by default as identity primary key,
  user_id uuid,
  week_start_date date not null,
  total_study_hours integer not null,
  topics_completed text not null,
  weak_subjects text not null,
  wins text not null,
  improvements text not null,
  next_week_plan text not null
);

create table if not exists public.monthly_reviews (
  id integer generated by default as identity primary key,
  user_id uuid,
  month text not null,
  total_study_hours integer not null,
  subject_summary text not null,
  habit_consistency integer not null,
  insight text not null,
  next_month_focus text not null
);

-- 1) Ensure user_id exists and is UUID on all app tables
alter table if exists public.tasks add column if not exists user_id uuid;
alter table if exists public.habits add column if not exists user_id uuid;
alter table if exists public.habit_logs add column if not exists user_id uuid;
alter table if exists public.study_sessions add column if not exists user_id uuid;
alter table if exists public.daily_reflections add column if not exists user_id uuid;
alter table if exists public.weekly_reviews add column if not exists user_id uuid;
alter table if exists public.monthly_reviews add column if not exists user_id uuid;

-- If a previous migration stored user_id as text, convert to uuid
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'tasks' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.tasks
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'habits' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.habits
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'habit_logs' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.habit_logs
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'study_sessions' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.study_sessions
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'daily_reflections' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.daily_reflections
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'weekly_reviews' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.weekly_reviews
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'monthly_reviews' AND column_name = 'user_id'
      AND data_type IN ('text', 'character varying')
  ) THEN
    ALTER TABLE public.monthly_reviews
      ALTER COLUMN user_id TYPE uuid USING NULLIF(user_id::text, '')::uuid;
  END IF;
END $$;

-- 2) Clean old constraints that were global (not user-scoped)
alter table if exists public.daily_reflections drop constraint if exists daily_reflections_date_key;
alter table if exists public.weekly_reviews drop constraint if exists weekly_reviews_week_start_date_key;
alter table if exists public.monthly_reviews drop constraint if exists monthly_reviews_month_key;

-- 3) Remove rows without a user (safe for fresh/local datasets)
delete from public.habit_logs where user_id is null;
delete from public.daily_reflections where user_id is null;
delete from public.weekly_reviews where user_id is null;
delete from public.monthly_reviews where user_id is null;
delete from public.study_sessions where user_id is null;
delete from public.tasks where user_id is null;
delete from public.habits where user_id is null;

-- 4) Enforce NOT NULL user ownership
alter table if exists public.tasks alter column user_id set not null;
alter table if exists public.habits alter column user_id set not null;
alter table if exists public.habit_logs alter column user_id set not null;
alter table if exists public.study_sessions alter column user_id set not null;
alter table if exists public.daily_reflections alter column user_id set not null;
alter table if exists public.weekly_reviews alter column user_id set not null;
alter table if exists public.monthly_reviews alter column user_id set not null;

-- 5) Add FK to Supabase auth.users
alter table if exists public.tasks
  drop constraint if exists tasks_user_id_fkey,
  add constraint tasks_user_id_fkey
  foreign key (user_id) references auth.users(id) on delete cascade;

alter table if exists public.habits
  drop constraint if exists habits_user_id_fkey,
  add constraint habits_user_id_fkey
  foreign key (user_id) references auth.users(id) on delete cascade;

alter table if exists public.habit_logs
