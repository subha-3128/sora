-- 6) Add user-scoped uniqueness
create unique index if not exists daily_reflections_user_date_uidx
  on public.daily_reflections (user_id, date);

create unique index if not exists weekly_reviews_user_week_uidx
  on public.weekly_reviews (user_id, week_start_date);

create unique index if not exists monthly_reviews_user_month_uidx
  on public.monthly_reviews (user_id, month);

create unique index if not exists habit_logs_user_habit_date_uidx
  on public.habit_logs (user_id, habit_id, date);

-- 7) Helpful query indexes
create index if not exists tasks_user_date_idx on public.tasks (user_id, date);
create index if not exists study_sessions_user_date_idx on public.study_sessions (user_id, date);
create index if not exists habits_user_idx on public.habits (user_id);

-- 8) Enable RLS
alter table public.tasks enable row level security;
alter table public.habits enable row level security;
alter table public.habit_logs enable row level security;
alter table public.study_sessions enable row level security;
alter table public.daily_reflections enable row level security;
alter table public.weekly_reviews enable row level security;
